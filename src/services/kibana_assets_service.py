    def create_dashboard(
        self,
        demo_slug: str,
        index_names: List[str],
        query_field: str = "query",
        duration_field: str = "response_time_ms",
        error_field: str = "error_code",
        score_field: str = "_score",
        company_name: str = "",
    ) -> Optional[str]:
        """
        Create a Kibana dashboard with four panels:
        1. Query volume over time (bar chart)
        2. P50/P95/P99 response latency (line chart)
        3. Top search terms (table)
        4. Error rate over time (area chart)

        Returns the dashboard ID or None on failure.
        """
        index_pattern = ",".join(index_names)
        title = f"{company_name or demo_slug} — Search Performance Dashboard"
        dashboard_id = str(uuid.uuid4())

        # Build panels using Kibana Lens saved object format
        panels = [
            self._query_volume_panel(index_pattern, query_field),
            self._latency_percentiles_panel(index_pattern, duration_field),
            self._top_search_terms_panel(index_pattern, query_field),
            self._error_rate_panel(index_pattern, error_field),
        ]

        dashboard_so = {
            "attributes": {
                "title": title,
                "description": f"Auto-generated by Elastic Demo Builder for {demo_slug}",
                "panelsJSON": json.dumps(panels),
                "optionsJSON": json.dumps({"useMargins": True, "syncColors": False}),
                "timeRestore": True,
                "timeTo": "now",
                "timeFrom": "now-7d",
                "refreshInterval": {"pause": False, "value": 30000},
                "kibanaSavedObjectMeta": {
                    "searchSourceJSON": json.dumps({
                        "query": {"query": "", "language": "kuery"},
                        "filter": [],
                    })
                },
            },
            "references": [],
        }

        try:
            resp = requests.post(
                f"{self._base_url}/api/saved_objects/dashboard/{dashboard_id}",
                headers=self.write_headers,
                json=dashboard_so,
                timeout=30,
            )
            if resp.status_code in (200, 201):
                logger.info(f"Created dashboard: {title} (id={dashboard_id})")
                return dashboard_id
            logger.warning(f"Dashboard creation returned {resp.status_code}: {resp.text[:300]}")
        except Exception as e:
            logger.warning(f"Could not create dashboard: {e}")

        return None

