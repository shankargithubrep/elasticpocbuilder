    # -------------------------------------------------------------------------
    # Slack Connector
    # -------------------------------------------------------------------------

    def ensure_slack_connector(self) -> Optional[str]:
        """
        Create a Kibana Slack webhook connector (deduplicated by name).
        Returns the connector ID or None if Slack not configured.
        """
        if not self.slack_webhook:
            logger.info("SLACK_WEBHOOK_URL not set — skipping Slack connector")
            return None

        connector_name = "Elastic Demo Builder — Slack"

        # Check if already exists
        existing_id = self._find_connector_by_name(connector_name)
        if existing_id:
            logger.info(f"Slack connector already exists: {existing_id}")
            return existing_id

        body = {
            "name": connector_name,
            "connector_type_id": ".slack",
            "config": {},
            "secrets": {"webhookUrl": self.slack_webhook},
        }

        try:
            resp = requests.post(
                f"{self._base_url}/api/actions/connector",
                headers=self.write_headers,
                json=body,
                timeout=30,
            )
            if resp.status_code in (200, 201):
                connector_id = resp.json().get("id")
                logger.info(f"Created Slack connector: {connector_id}")
                return connector_id
            logger.warning(f"Slack connector creation returned {resp.status_code}: {resp.text[:300]}")
        except Exception as e:
            logger.warning(f"Could not create Slack connector: {e}")

        return None

    def _find_connector_by_name(self, name: str) -> Optional[str]:
        try:
            resp = requests.get(
                f"{self._base_url}/api/actions/connectors",
                headers=self.headers,
                timeout=15,
            )
            if resp.status_code == 200:
                for conn in resp.json():
                    if conn.get("name") == name:
                        return conn.get("id")
        except Exception:
            pass
        return None

    # -------------------------------------------------------------------------
    # Alerting Rules
    # -------------------------------------------------------------------------

    def create_alert_rules(
        self,
        demo_slug: str,
        index_names: List[str],
        slack_connector_id: Optional[str],
        duration_field: str = "response_time_ms",
        p95_threshold_ms: int = 2000,
        error_threshold: int = 10,
        company_name: str = "",
    ) -> List[str]:
        """
        Create two alerting rules:
        1. High P95 latency alert (ES|QL threshold)
        2. High error rate alert (ES|QL threshold)

        Returns list of created rule IDs.
        """
        rule_ids = []
        index_pattern = ",".join(index_names)
        actions = []

        if slack_connector_id:
            actions = [
                {
                    "id": slack_connector_id,
                    "group": "threshold met",
                    "params": {
                        "message": (
                            f"🚨 *{company_name or demo_slug} Alert* — "
                            "{{context.message}}\n"
                            f"Index: `{index_pattern}`\n"
                            "Check Kibana for details."
                        )
                    },
                }
            ]

        # Rule 1: High P95 latency
        p95_rule = {
            "name": f"{company_name or demo_slug} — High P95 Search Latency",
            "rule_type_id": ".es-query",
            "consumer": "alerts",
            "schedule": {"interval": "5m"},
            "params": {
                "index": index_names,
                "timeField": "@timestamp",
                "timeWindowSize": 5,
                "timeWindowUnit": "m",
                "thresholdComparator": ">",
                "threshold": [p95_threshold_ms],
                "esQuery": json.dumps({
                    "aggs": {
                        "p95": {
                            "percentiles": {
                                "field": duration_field,
                                "percents": [95],
                            }
                        }
                    },
                    "size": 0,
                }),
            },
            "actions": actions,
            "tags": [demo_slug, "search", "latency", "demo-builder"],
        }

        # Rule 2: High error count
        error_rule = {
            "name": f"{company_name or demo_slug} — High Search Error Rate",
            "rule_type_id": ".es-query",
            "consumer": "alerts",
            "schedule": {"interval": "5m"},
            "params": {
                "index": index_names,
                "timeField": "@timestamp",
                "timeWindowSize": 5,
                "timeWindowUnit": "m",
                "thresholdComparator": ">",
                "threshold": [error_threshold],
                "esQuery": json.dumps({
                    "query": {
                        "bool": {
                            "must_not": [{"term": {"status_code": 200}}],
                            "filter": [{"exists": {"field": "status_code"}}],
                        }
                    },
                    "size": 0,
                }),
            },
            "actions": actions,
            "tags": [demo_slug, "search", "errors", "demo-builder"],
        }

        for rule_body in [p95_rule, error_rule]:
            rule_id = self._create_alert_rule(rule_body)
            if rule_id:
                rule_ids.append(rule_id)

        return rule_ids

    def _create_alert_rule(self, rule_body: Dict[str, Any]) -> Optional[str]:
        try:
            resp = requests.post(
                f"{self._base_url}/api/alerting/rule",
                headers=self.write_headers,
                json=rule_body,
                timeout=30,
            )
            if resp.status_code in (200, 201):
                rule_id = resp.json().get("id")
                logger.info(f"Created alert rule: {rule_body['name']} (id={rule_id})")
                return rule_id
            logger.warning(f"Alert rule creation returned {resp.status_code}: {resp.text[:300]}")
        except Exception as e:
            logger.warning(f"Could not create alert rule '{rule_body.get('name')}': {e}")
        return None

    # -------------------------------------------------------------------------
    # Case Management
    # -------------------------------------------------------------------------

    def configure_case_connector(
        self,
        slack_connector_id: Optional[str],
    ) -> bool:
        """
        Configure Kibana Cases to use the Slack connector as the default
        so that alerts auto-create cases.
        """
        if not slack_connector_id:
            return False

        body = {
            "connector": {
                "id": slack_connector_id,
                "name": "Elastic Demo Builder — Slack",
                "type": ".slack",
                "fields": None,
            },
            "closure_type": "close-by-user",
        }

        try:
            resp = requests.patch(
                f"{self._base_url}/api/cases/configure",
                headers=self.write_headers,
                json=body,
                timeout=30,
            )
            if resp.status_code in (200, 201):
                logger.info("Configured Cases connector")
                return True
            logger.warning(f"Cases config returned {resp.status_code}: {resp.text[:200]}")
        except Exception as e:
            logger.warning(f"Could not configure Cases connector: {e}")

        return False

    # -------------------------------------------------------------------------
    # Data Views (index patterns)
    # -------------------------------------------------------------------------

    def ensure_data_views(
        self,
        index_names: List[str],
    ) -> List[str]:
        """
        Create Kibana Data Views for each index so Discover and dashboards work.
        Idempotent — skips if already exists.
        Returns list of created data view IDs.
        """
        created = []
        for index_name in index_names:
            view_id = self._ensure_data_view(index_name)
            if view_id:
                created.append(view_id)
        return created

    def _ensure_data_view(self, index_name: str) -> Optional[str]:
        if not self.kibana_url:
            return None

        # Check if data view already exists for this index
        try:
            resp = requests.get(
                f"{self._base_url}/api/data_views",
                headers=self.headers,
                timeout=15,
            )
            if resp.status_code == 200:
                for dv in resp.json().get("data_view", []):
                    if dv.get("title", "") in (index_name, f"{index_name}*"):
                        logger.info(f"Data view already exists for: {index_name}")
                        return dv.get("id")
        except Exception:
            pass

        # Create new data view
        body = {
            "data_view": {
                "title": index_name,
                "name": index_name,
                "timeFieldName": "@timestamp",
            }
        }

        try:
            resp = requests.post(
                f"{self._base_url}/api/data_views/data_view",
                headers=self.write_headers,
                json=body,
                timeout=20,
            )
            if resp.status_code in (200, 201):
                view_id = resp.json().get("data_view", {}).get("id")
                logger.info(f"Created data view: {index_name} (id={view_id})")
                return view_id
            logger.warning(f"Data view creation returned {resp.status_code}: {resp.text[:200]}")
        except Exception as e:
            logger.warning(f"Could not create data view for {index_name}: {e}")

        return None

