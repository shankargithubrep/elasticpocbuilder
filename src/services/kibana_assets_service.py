"""
Kibana Assets Service

Creates and manages all Kibana-side assets for Search demos:
- Dashboards (P95 latency, query volume, error rates, relevance distribution)
- Slack connector
- Alerting rules (ES|QL threshold)
- Case management connector configuration
- Saved Discover views
- Scoped read-only API key
"""

import json
import logging
import os
import uuid
from typing import Dict, List, Any, Optional
import requests
from elasticsearch import Elasticsearch
from dotenv import load_dotenv

load_dotenv()
logger = logging.getLogger(__name__)


class KibanaAssetsService:
    """
    Manages Kibana assets for Search demos via the Kibana REST API.
    All operations are idempotent (tag-based deduplication).
    """

    def __init__(self, es_client: Optional[Elasticsearch] = None):
        self.kibana_url = os.getenv("ELASTICSEARCH_KIBANA_URL", "").rstrip("/")
        self.api_key = os.getenv("ELASTICSEARCH_API_KEY", "")
        self.slack_webhook = os.getenv("SLACK_WEBHOOK_URL", "")
        self.slack_channel = os.getenv("SLACK_CHANNEL", "#elastic-demos")
        self.space_id = os.getenv("KIBANA_SPACE_ID", "default")
        self.es = es_client

        self.headers = {
            "Authorization": f"ApiKey {self.api_key}",
            "Content-Type": "application/json",
        }
        self.write_headers = {**self.headers, "kbn-xsrf": "true"}

    @property
    def _base_url(self) -> str:
        if self.space_id and self.space_id != "default":
            return f"{self.kibana_url}/s/{self.space_id}"
        return self.kibana_url

